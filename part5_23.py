def print_sudoku (sudoku) :
    for i in range(len(sudoku)) :
        for j in range(len(sudoku[0])) :
            if (sudoku[i][j] == 0)  :   print ("_", end = " ")
            else                    :   print (sudoku[i][j], end = " ")
        
        print ()


